				<h2><span>監修</span> Studio Dede</h2>
				<p>
					ライブハウスに演奏を聞きに行くと今日は音が良かったとか、悪かったとか皆さん経験あると思います。<br />
					それではミュージシャンがライブハウスで良い演奏をする（良い音を出す）場合に一番重要なのは、、、<br />
					それはステージの中音です。<br />
					ミュージシャンにとってはステージ上のモニター含め中音のバランスが悪かったら良い演奏が出来ません。<br />
					アメリカなどではステージ中音をベストな状態にするモニターエンジニアという職業があると共にモニターエンジニアが一番重要なポジションだとされています。<br />
					<br />
					話を戻しますがPPCの音響を任され一番に考えた事はミュージシャンが演奏しやすい環境を作る事です。<br />
					演奏しやすい＝ミュージシャン同士がバランスがとれた演奏をしている。<br />
					<br />
					そしてそのバランスでお客さんも演奏を楽しめる。<br />
					<br />
					これがPPC アートサロンのサウンドのコンセプトです。<br />
					<br />
					ここで数多くの若いミュージシャンがジャムセッションを行なったり、<br />数多くのミュージシャンの全国ツアーの会場の１つとしてPPC アートサロンを利用して頂くことを願ってます。
